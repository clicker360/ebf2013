func (e *Entidad) Key(c appengine.Context) *datastore.Key {
	return datastore.NewKey(c, "Entidad", e.CveEnt, 0, nil)
}

func (m *Municipio) Parent(c appengine.Context) *Entidad {
	e, _ := GetEntidad(c, m.CveEnt)
	return e
}

func (l *Localidad) Parent(c appengine.Context) *Municipio {
	return GetMunicipio(c, l.CveMun)
}

func (m *Municipio) Key(c appengine.Context) *datastore.Key {
	return datastore.NewKey(c, "Municipio", m.CveMun, 0, m.Parent(c).Key(c))
}

func (l *Localidad) Key(c appengine.Context) *datastore.Key {
	return datastore.NewKey(c, "Localidad", l.CveLoc, 0, l.Parent(c).Key(c))
}

func GetEntidad(c appengine.Context, cveent string) (*Entidad, error) {
	e := &Entidad{ CveEnt: cveent }
	err := datastore.Get(c, e.Key(c), e)
	if err == datastore.ErrNoSuchEntity {
		return nil, err
	}
	return e, nil
}

func GetMunicipio(c appengine.Context, cvemun string) *Municipio {
	q := datastore.NewQuery("Municipio").Filter("CveMun =", cvemun)
	for i := q.Run(c); ; {
		var m Municipio
		_, err := i.Next(&m)
		if err == datastore.Done {
			break
		}
		return &m
	}
	return nil
}

func GetLocalidad(c appengine.Context, cveloc string) *Localidad {
	q := datastore.NewQuery("Localidad").Filter("CveLoc =", cveloc)
	for i := q.Run(c); ; {
		var l Localidad
		_, err := i.Next(&l)
		if err == datastore.Done {
			break
		}
		return &l
	}
	return nil
}

func (e *Entidad) GetMunicipios(c appengine.Context) (*[]Municipio, error) {
	q := datastore.NewQuery("Municipio").Ancestor(e.Key(c))
	nm, _ := q.Count(c)
	municipios := make([]Municipio, 0, nm)
	if _, err := q.GetAll(c, &municipios); err != nil {
		if err == datastore.ErrNoSuchEntity {
			return nil, err
		}
	}
	return &municipios, nil
}

func (m *Municipio) GetLocalidades(c appengine.Context) (*[]Localidad, error) {
	q := datastore.NewQuery("Localidad").Ancestor(m.Key(c))
	nl, _ := q.Count(c)
	localidades := make([]Localidad, nl)
	if _, err := q.GetAll(c, &localidades); err != nil {
		if err == datastore.ErrNoSuchEntity {
			return nil, err
		}
	}
	return &localidades, nil
}

